namespace LeagueSharp
{
    /// <summary>
    ///     GameObjectCombatType enum, represents the type of combat unit.
    /// </summary>
    public enum GameObjectCombatType
    {
        /// <summary>
        ///     Melee type.
        ///     Melee units are short-range auto-attack combat units.
        /// </summary>
        Melee = 1,

        /// <summary>
        ///     Ranged type.
        ///     Ranged units are long-range auto-attack combat units.
        /// </summary>
        Ranged = 2
    }
}